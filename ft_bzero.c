#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	int		null;
	char	*str;

	i = 0;
	null = '\0';
	str = s;
	while (i < n)
	{
		str[i] = null;
		i++;
	}
}
